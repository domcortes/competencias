     * @param  array  $data
     * @return \Illuminate\Http\RedirectResponse
     */
    protected function create(Request $request)
    {
        User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'role' => 'atleta'
        ]);

        return redirect()->route('home');
    }

